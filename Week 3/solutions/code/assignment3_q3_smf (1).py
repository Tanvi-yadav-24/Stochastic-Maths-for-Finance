import numpy as np
import matplotlib.pyplot as plt

# Parameters
X0 = 3.0
theta = 2.0
sigma = 0.5

T = 5
N = 5000
M = 10

dt = T/N

t = np.linspace(0,T,N+1)

paths = np.zeros((M,N+1))
paths[:,0] = X0

for i in range(M):
    for k in range(N):
        Z = np.random.randn()

        paths[i,k+1] = (
            paths[i,k]
            - theta*paths[i,k]*dt
            + sigma*np.sqrt(dt)*Z
        )

# Plot paths
for i in range(M):
    plt.plot(t,paths[i])

# Stationary mean
plt.axhline(0,color='black',
            linestyle='--',
            label='Mean')

# ±2 std band
std_inf = sigma/np.sqrt(2*theta)

plt.fill_between(
    t,
    -2*std_inf,
    2*std_inf,
    alpha=0.2,
    label='±2σ band'
)

plt.xlabel("Time")
plt.ylabel("X(t)")
plt.legend()
plt.show()

