X = np.full(M,X0,dtype=float)

for _ in range(N):

    Z = np.random.randn(M)

    X += -theta*X*dt \
         + sigma*np.sqrt(dt)*Z

XT = X

# Histogram
plt.hist(XT,
         bins=100,
         density=True,
         alpha=0.6)

# Theoretical density
x = np.linspace(min(XT),
                max(XT),
                1000)

var_theory = sigma**2/(2*theta)

pdf = norm.pdf(
        x,
        loc=0,
        scale=np.sqrt(var_theory)
      )

